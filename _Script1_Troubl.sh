#INICIO DO SCRIPT

echo ""
echo Segue troubleshooting do host: $1

echo "================================================================================================="
echo ""
echo "#HOST"
host $1
echo ""
echo "================================================================================================="

echo ""
echo "#PING"
ping -c5 $1
echo ""
echo "================================================================================================="


echo ""
echo "#PINGv6"
ping6 -c5 $1
echo ""
echo "================================================================================================="


echo ""
echo "#TRACE"
mtr -rn $1
echo "================================================================================================="
echo ""


echo ""
echo "#NMAP"
nmap -4 -p 22,53,80,443,8080 -Pn $1
echo "================================================================================================="

